#!/bin/bash

### Define shell options

# enable to use variable
shopt -s cdable_vars
# ignore miss spell (cd)
shopt -s cdspell
# ignore miss spell (dir)
shopt -s dirspell
# append history on exit
shopt -s histappend
# show jobs on exit
shopt -s checkjobs
# update window size on command run
shopt -s checkwinsize
